package cz.auderis.corusco.examples.generated;

/**
 * Sample enum used by generated combo-box examples.
 *
 * <p>The constants are ordinary domain values. The examples use them to show
 * that a {@code @ComboBox} record component can expose an enum value while the
 * generated metadata and Swing binding code remain separate from the domain
 * enum itself.</p>
 */
public enum GeneratedCustomerType {
    /**
     * Retail customer category.
     */
    RETAIL,

    /**
     * Business customer category.
     */
    BUSINESS
}
